
x=50
y=30

#arithmatics operators
print("---Arithmetic operators---")
print("sum :", (x+y))
print("sub :", (x-y))
print("mul :", (x*y))
print("div :", (x/y))
print("mod :", (x%y))

#relational operators
print("---relational operators---")
print("x > y :", (x>y))
print("x < y :", (x<y))
print("x >= y :", (x>=y))
print("x <= y :", (x<=y))
print("x == y :", (x==y))
print("x != y :", (x!=y))

